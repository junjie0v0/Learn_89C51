#include <REGX51.H>
#include "A.H"

#define MATRIX_LED_PORT P0 //宏定义P0口

unsigned char text[]={0x1D,0x55,0x95,0xF7,0x08,0xFF,0x5A,0x29,0x48,0x00,0x55,0xF5,0xBF,0xF5,0x1F,0x00,
0x00,0x08,0x04,0xFA,0x49,0x32,0x34,0x48,0x04,0x00,0x00,0x00,0x00,0x00,0x00,0x00,
0x00,0x00,0x00,};

sbit SCK=P3^6;		//SRCLK 上升沿移位(置1进行移位，移位完进行置0)
sbit RCK=P3^5;		//RCLK 置1对穿行寄存器的数据进行搬运

void main()
{
	char i=0,j=0,conut=0;
	SCK=0;
	RCK=0;//74HC595初始化；
	while(1){
		for(i=0;i<8;i++)
		{
			MatrixLED_ShowColumn(i,text[i+j]);
		}
		conut++;
		if(conut>=10)
		{
			conut=0;
			j++;
		}
		if(j>=26)
			j=0;
}
}